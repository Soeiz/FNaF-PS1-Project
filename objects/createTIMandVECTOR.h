//Function that create a TIM object.
//
//object : Name of the TIM Image 
//object2 : Name of the TIM Image (???)
//objectName : Name of the object (poly)
//objectRotation : Rotation of the object (RotVector)
//objectXYZcoord : Coordinates of the object in X, Y, Z (MovVector)
//objectScale : Scale of the object (ScaleVector, "ONE" normally)
//objectVertPos : Position of each point (up left, down left, up right, down right) (VertPos)
//objectPolyMatrix : Matrix of the object (PolyMatrix)
//
//DRAFT (doesn't work for most of images)
void createTIM(TIM_IMAGE * object, TIM_IMAGE object2, POLY_FT4 *objectName, SVECTOR objectRotation, VECTOR objectXYZcoord, VECTOR objectScale, SVECTOR * objectVertPos, MATRIX objectPolyMatrix) {

    objectName = (POLY_FT4 *)nextpri;                 
            
    RotMatrix(&objectRotation, &objectPolyMatrix);    
    TransMatrix(&objectPolyMatrix, &objectXYZcoord);  
    ScaleMatrix(&objectPolyMatrix, &objectScale);
    
    SetRotMatrix(&objectPolyMatrix);                  
    SetTransMatrix(&objectPolyMatrix);                
    
    setClut(objectName, 960, 68);
    
    setRGB0(objectName, InAndOut_alpha, InAndOut_alpha, InAndOut_alpha); 

    setPolyFT4(objectName);                           
    
    objectName->tpage = getTPage(object -> mode&0x3, 0, object2.prect->x, object2.prect->y); 
    
    RotTransPers4(
                &objectVertPos[0],      &objectVertPos[1],      &objectVertPos[2],      &objectVertPos[3],
                (long*)&objectName->x0, (long*)&objectName->x1, (long*)&objectName->x2, (long*)&objectName->x3,
                &polydepth,
                &polyflag
                );                               
    
    setUV4(objectName, 0, 0, 0, 55, 240, 0, 240, 55);
        
    addPrim(ot[db], objectName);                       
    
    nextpri += sizeof(POLY_FT4); 

}